-- Прибираємо «переходи» ботів (бот-блеф), щоб видимі пули точно дорівнювали
-- заданим кількостям ботів на хід. Інакше деякі пули показували 0, хоча ботів
-- на цей хід призначено >0 (вони показували інший хід). Адмін-збереження таблиці
-- (rps_admin_set_script) також обнуляє ці переходи.
update public.rps_bot_script
   set r2s = 0, r2p = 0, s2r = 0, s2p = 0, p2r = 0, p2s = 0;
